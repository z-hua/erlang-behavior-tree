-module(monster_util).

%% API
-compile([export_all]).
-compile(nowarn_export_all).

%%%-----------------------------------------------------------------------------
%%% API Functions
%%%-----------------------------------------------------------------------------
-define(k_hp, hp).
get_hp() ->
	get(?k_hp).

set_hp(Hp) ->
	put(?k_hp, Hp).

del_hp() ->
	set_hp(get_hp() - 1).


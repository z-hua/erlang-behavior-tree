-module(bnode_failure).

-include("btree.hrl").

%% API
-export([forward/2]).
-export([backward/2]).

%%%-----------------------------------------------------------------------------
%%% API Functions
%%%-----------------------------------------------------------------------------
forward(Tree, Node) ->
    #bn{children=[ChildID]} = Node,
    bnode_behavior:forward(Tree, ChildID).

backward(Tree, Node) ->
    bnode_behavior:backward(Tree#bt{result=?SUCCESS}, Node).

